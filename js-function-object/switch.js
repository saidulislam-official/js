
// condition statement
var colorPick1 = 'yellow';

if (colorPick1 === 'blue') {
    console.log('Color is blue');
}
else if (colorPick1 === 'red') {
    console.log('Color is red');
}
else if (colorPick1 === 'green') {
    console.log('Color is green');
}
else if (colorPick1 === 'white') {
    console.log('Color is white');
}
else if (colorPick1 === 'black') {
    console.log('Color is black');
}
else {
    console.log('Color is yellow');
}


// switch case
var colorPick2 = 'blue';
switch (colorPick2) {
    case 'blue':
        console.log('Color is blue');
        break;
    case 'red':
        console.log('Color is red');
        break;
    case 'green':
        console.log('Color is green');
        break;
    case 'white':
        console.log('Color is white');
        break;
    case 'black':
        console.log('Color is black');
        break;
    default:
        console.log('Color is yellow');
}