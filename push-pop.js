var number = [11, 22, 33, 44, 55, 66, 77, 88, 99];

//use push to add element in array as the last element array

number.push(111);

// console.log(number);

//use pop to remove element from array as the last element array
number.pop();

// console.log(number);

//use unshift to add element in array as the first element array
number.unshift(5);

// console.log(number);

//use shift to remove element from array as the first element arrayf
number.shift(); 

