
// variable declaration
var age = 25;

// variable value update
age = 26;

//add something to the previous value
var price1 = 82;
var price2 = 48;

//set a new value
price1 = 120;

// add some value to the existing value
price1 = price1 + 35;
// console.log(price1);


// addition to the same variable shorthand
// += means adding some value to the existing value
price1 += 3;
console.log(price1);

// shorthand subtraction
// -= means subtracting some value from the existing value
price2 -= 5;
console.log(price2);

// shorthand multiplication
// *= means multiplying some value to the existing value
price2 *= 2;
console.log(price2);

// shorthand division
// /= means dividing some value from the existing value
price2 /= 2;
console.log(price2);

// 
var price3 = 40;
// ++ means adding 1 to the existing value
price3++;
console.log(price3);

// -- means subtracting 1 from the existing value
price3--;
console.log(price3);







